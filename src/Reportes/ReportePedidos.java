    private void ValidarServicio() {
        if (cboServicios.getSelectedIndex() == 0) {
            Mensajes.msjMuestra("Seleccione un Servicio");
        } else if (cboServicios.getSelectedIndex() == 1) {
            Servicio = "RR";
            Funcion_Busqueda();
        } else if (cboServicios.getSelectedIndex() == 2) {
            Servicio = "RL";
            Funcion_Busqueda();
        } else if (cboServicios.getSelectedIndex() == 3) {
            Servicio = "VD";
            Funcion_Busqueda();
        } else if (cboServicios.getSelectedIndex() == 4) {
            Servicio = "VP";
            Funcion_Busqueda();
        } else if (cboServicios.getSelectedIndex() == 5) {
            Servicio = "VA";
            Funcion_Busqueda();
        } else if (cboServicios.getSelectedIndex() == 6) {
            Servicio = "CT";
            Funcion_Busqueda();
        }
    }

    private void Funcion_Busqueda() {
        Date fechaDesde = jDateDesde.getDate();
        Date fechaHasta = jDateHasta.getDate();

        if (fechaDesde == null || fechaHasta == null) {
            Mensajes.msjError("Complete las Fechas correctamente");
        } else if (fechaDesde.before(fechaHasta) || fechaDesde.equals(fechaHasta)) {

            SimpleDateFormat formatoInicio = new SimpleDateFormat("yyyy-MM-dd 00:00:00");
            SimpleDateFormat formatoFinal = new SimpleDateFormat("yyyy-MM-dd 23:59:59");
            String Desde = formatoInicio.format(fechaDesde);
            String Hasta = formatoFinal.format(fechaHasta);
            String servicio = Servicio;

            if (rbtnActualizados.isSelected() == true) {
                if (servicio.equals("VP")) {
                    System.out.println("Entro a proveedor ACTUALIZADOS");
                    tblDatos.setModel(DbUtils.resultSetToTableModel(Detalle_PedidoDAO.ReportesActualizados_2(servicio, Desde, Hasta)));
                    ValidarDatosenTabla();
                } else {
                    tblDatos.setModel(DbUtils.resultSetToTableModel(Detalle_PedidoDAO.ReportesActualizados(servicio, Desde, Hasta)));
                    ValidarDatosenTabla();
                }
            } else if (rbtnEliminados.isSelected() == true) {
                if (servicio.equals("VP")) {
                    System.out.println("Entro a proveedor eLIMINADOS");
                    tblDatos.setModel(DbUtils.resultSetToTableModel(Detalle_PedidoDAO.ReportesEliminados_2(servicio, Desde, Hasta)));
                    ValidarDatosenTabla();
                } else {
                    tblDatos.setModel(DbUtils.resultSetToTableModel(Detalle_PedidoDAO.ReportesEliminados(servicio, Desde, Hasta)));
                    ValidarDatosenTabla();
                }
            }

        } else {
            Mensajes.msjError("El parametro de fecha no es correcto");
        }
    }
