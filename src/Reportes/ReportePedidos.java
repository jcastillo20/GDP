    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        // TODO add your handling code here:
        JFileChooser dialog = new JFileChooser();
        int opcion = dialog.showSaveDialog(ReportePedidos.this);
        String detalle="";

        if (opcion == JFileChooser.APPROVE_OPTION) {

            File dir = dialog.getSelectedFile();

            try {
                List<JTable> tb = new ArrayList<JTable>();
                tb.add(tblDatos);
                //-------------------
                if(rbtnActualizados.isSelected()==true){
                    detalle="Reportes Actualizados";
                }else if(rbtnEliminados.isSelected()==true){
                    detalle="Reportes Eliminados";
                }
                export_excel excelExporter = new export_excel(tb, new File(dir.getAbsolutePath() + ".Xls"));
                if (excelExporter.export_ReportePedido(detalle)) {
                    JOptionPane.showMessageDialog(null, "TABLAS EXPORTADOS CON EXITOS!");

                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnGenerarActionPerformed

